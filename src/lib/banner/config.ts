/** Public roles allowed to create banner ads (builders promote their projects). */
export const BANNER_ELIGIBLE_ROLES = ["builder"] as const;
export function isBannerEligibleRole(role: string | undefined): boolean {
  return !!role && (BANNER_ELIGIBLE_ROLES as readonly string[]).includes(role);
}

/** Public-display predicate, mirrored from the RLS policy (defense in depth). */
export interface DisplayableInput {
  approval_status: string;
  status: string;
  is_paused: boolean;
  payment_status: string;
  start_date: string | null;
  end_date: string | null;
}
export function isPubliclyDisplayable(a: DisplayableInput, today = new Date()): boolean {
  if (a.approval_status !== "approved" || a.status !== "active" || a.is_paused) return false;
  if (!["not_required", "success"].includes(a.payment_status)) return false;
  if (!a.start_date || !a.end_date) return false;
  const d = today.toISOString().slice(0, 10);
  return a.start_date <= d && a.end_date >= d;
}

