/** Has this ad passed its end date? (used to render Expired in dashboards) */
export function isExpired(end_date: string | null, today = new Date()): boolean {
  if (!end_date) return false;
  return end_date < today.toISOString().slice(0, 10);
}

/** ₹ short format for the project card (single value). */
export function inr(v: number | null | undefined): string | null {
  if (!v) return null;
  if (v >= 10000000) return `₹${(v / 10000000).toFixed(2).replace(/\.00$/, "")} Cr`;
  if (v >= 100000) return `₹${(v / 100000).toFixed(2).replace(/\.00$/, "")} L`;
  return `₹${v.toLocaleString("en-IN")}`;
}

/** Price-range label from a min/max (units) or single price. */
export function priceRangeLabel(min: number | null, max: number | null, fallback: number | null): string | null {
  const lo = inr(min ?? fallback);
  const hi = inr(max ?? null);
  if (lo && hi && min !== max) return `${lo} - ${hi}`;
  return lo ?? null;
}

/** "2, 3 BHK" style label from distinct BHK values. */
export function bhkLabel(bhks: number[]): string | null {
  const uniq = [...new Set(bhks.filter((b) => b && b > 0))].sort((a, b) => a - b);
  if (uniq.length === 0) return null;
  return `${uniq.join(", ")} BHK`;
}

/**
 * Internal-only destination rule. External links are disabled, so a destination
 * must be a same-site path under an allowed prefix.
 */
const ALLOWED_DEST_PREFIXES = ["/search", "/property/", "/project/", "/pricing"];
export function validateDestination(raw: string): { ok: true; url: string } | { ok: false; message: string } {
  const url = (raw || "").trim();
  if (!url.startsWith("/") || url.startsWith("//")) {
    return { ok: false, message: "Destination must be an internal link starting with “/”." };
  }
  if (!ALLOWED_DEST_PREFIXES.some((p) => url === p || url.startsWith(p))) {
    return { ok: false, message: `Link must point to ${ALLOWED_DEST_PREFIXES.join(", ")}.` };
  }
  return { ok: true, url };
}
